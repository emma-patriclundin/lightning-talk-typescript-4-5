/**************************************
 ** Supporting lib from node_modules **
 **************************************
 *
 * @link https://www.typescriptlang.org/docs/handbook/release-notes/typescript-4-5.html#supporting-lib-from-node_modules
 *
 * Background:
 * TypeScript bundles a series of declaration files (.d.ts files) to represent the available APIs in the JavaScript language.
 * We can choose what files to include using the "lib" setting in tsconfig.json file.
 *
 * Problem:
 * When you upgrade TypeScript, you’re also forced to handle changes to TypeScript’s built-in declaration files,
 * and this can be a challenge when the DOM APIs change as frequently as they do.
 *
 *
 * Solution:
 * When deciding which lib files TypeScript should include,
 * Typescript will first look for a scoped @typescript/lib-* package in node_modules
 *
 * *********
 * @description lib setting
 * Specify a set of bundled library declaration files
 * that describe the target runtime environment
 *
 * File: tsconfig.json
 * "lib": ["dom"]
 * **********
 *
 * **********
 * File: package.json
 * "dependencies": {
 *  "@typescript/lib-dom": "npm:@types/web@0.0.46",
 * }
 * **********
 */

